const config = require('../config');
const { cmd } = require('../command');
const axios = require('axios');

// Text to Music API configuration
const textToMusicAPI = {
    baseURL: "https://api.privatezia.biz.id/api/ai/texttomusic"
};

cmd({
    pattern: "aisong",
    alias: ["texttomusic", "ttm", "aimusic"],
    react: "🎵",
    desc: "Generate music from text prompt",
    category: "AI",
    use: "<prompt>",
    filename: __filename
}, async (conn, mek, m, { from, q, reply }) => {
    try {
        if (!q) {
            return await conn.sendMessage(from, {
                image: { url: config.BOTIMAGE || 'https://mrfrankk-cdn.hf.space/mrfrank/mini/aisong.png' },
                caption: `🎵 *AI Song Generator*\n\n✍️ *Please provide a text prompt to generate music!*\n\n*Examples:*\n• ${config.PREFIX || '.'}aisong sad piano melody 30 seconds\n• ${config.PREFIX || '.'}aisong upbeat electronic dance music\n• ${config.PREFIX || '.'}aisong relaxing ambient background music\n\n${config.FOOTER || '© Created By Mr Frank OFC'}`
            }, { quoted: mek });
        }

        // ⏳ React - processing
        await conn.sendMessage(from, { react: { text: '⏳', key: mek.key } });
        
        // Send processing message with image
        await conn.sendMessage(from, {
            image: { url: config.BOTIMAGE || 'https://mrfrankk-cdn.hf.space/mrfrank/mini/aisong.png' },
            caption: `🎵 *Generating Your AI Music...*\n\n📝 *Prompt:* ${q}\n\n⏳ Please wait while we create your music masterpiece!\n\n${config.FOOTER || '© Created By Mr Frank OFC'}`
        }, { quoted: mek });

        // Generate music from text prompt
        const apiUrl = `${textToMusicAPI.baseURL}?prompt=${encodeURIComponent(q)}`;
        
        const res = await axios.get(apiUrl, {
            timeout: 60000, // Longer timeout for music generation
            headers: {
                'User-Agent': 'Mozilla/5.0 (Windows NT 10.0; Win64; x64) AppleWebKit/537.36 (KHTML, like Gecko) Chrome/120.0.0.0 Safari/537.36',
                'accept': '*/*'
            }
        });

        if (!res.data || !res.data.status || !res.data.result || !res.data.result.url) {
            await conn.sendMessage(from, { react: { text: '❌', key: mek.key } });
            return await conn.sendMessage(from, {
                image: { url: config.BOTIMAGE || 'https://mrfrankk-cdn.hf.space/mrfrank/mini/aisong.png' },
                caption: `❌ *Music Generation Failed*\n\n📝 *Prompt:* ${q}\n\n💡 *Possible solutions:*\n• Try a different music description\n• Make your prompt more specific\n• Try again in a few minutes\n\n${config.FOOTER || '© Created By Mr Frank OFC'}`
            }, { quoted: mek });
        }

        const musicData = res.data.result;

        // Prepare caption with config.FOOTER
        const caption = `🎵 *AI Generated Music Successfully!*\n\n` +
                      `📝 *Prompt:* ${q}\n` +
                      `🎼 *Generated by:* AI Music Model\n` +
                      `🎧 *Format:* MP3 Audio\n\n` +
                      `> ${config.FOOTER || '© Created By Mr Frank OFC'}`;

        // Send the generated music
        await conn.sendMessage(from, {
            audio: { url: musicData.url },
            mimetype: 'audio/mpeg',
            fileName: `ai_music_${Date.now()}.mp3`,
            caption: caption
        }, { quoted: mek });

        // ✅ React - success
        await conn.sendMessage(from, { react: { text: '✅', key: mek.key } });

    } catch (error) {
        console.error('[TEXT_TO_MUSIC] Command Error:', error?.message || error);
        
        await conn.sendMessage(from, { react: { text: '❌', key: mek.key } });
        
        const errorCaption = `❌ *Music Generation Error*\n\n` +
                           `📝 *Prompt:* ${q || 'N/A'}\n\n` +
                           `💡 *Possible reasons:*\n` +
                           `• AI service is temporarily busy\n` +
                           `• Prompt was too complex/vague\n` +
                           `• Network connection issue\n` +
                           `• Server timeout\n\n` +
                           `🔄 Please try again with a different description.\n\n` +
                           `> ${config.FOOTER || '© Created By Mr Frank OFC'}`;
        
        await conn.sendMessage(from, {
            image: { url: config.BOTIMAGE || 'https://mrfrankk-cdn.hf.space/mrfrank/mini/aisong.png' },
            caption: errorCaption
        }, { quoted: mek });
    }
});
