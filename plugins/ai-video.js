/*
Sora AI Video Generator Command for Subzero MD
Generates a short AI video from a text prompt using Okatsu API
*/

const axios = require('axios');
const { cmd } = require('../command');
const config = require('../config');

cmd({
    pattern: "sora",
    alias: ["soravideo", "aivideo", "texttovideo"],
    desc: "Generate AI video from a text prompt",
    category: "ai",
    react: "🎬",
    use: "<prompt>",
    filename: __filename
}, async (conn, mek, m, { from, quoted, body, args, reply }) => {
    try {
        // Get user input
        const input = args.join(" ") || (quoted && (quoted.text || quoted.caption)) || null;

        if (!input) {
            return await reply(`🎬 *Sora AI Video Generator*\n\n✍️ *Please provide a prompt!*\n\n*Example:*\n${config.PREFIX || '.'}sora anime girl with short blue hair\n\n${config.FOOTER || '© Created By Mr Frank OFC'}`);
        }

        // Send processing reaction
        await conn.sendMessage(from, { react: { text: '⏳', key: mek.key } });
        
        await reply("🎬 Generating your AI video... This may take a moment!");

        // API URL
        const apiUrl = `https://okatsu-rolezapiiz.vercel.app/ai/txt2video?text=${encodeURIComponent(input)}`;

        // Request to API
        const { data } = await axios.get(apiUrl, {
            timeout: 120000, // 2 minutes timeout for video generation
            headers: { 
                'User-Agent': 'Mozilla/5.0 (Windows NT 10.0; Win64; x64) AppleWebKit/537.36 (KHTML, like Gecko) Chrome/120.0.0.0 Safari/537.36',
                'accept': '*/*'
            }
        });

        const videoUrl = data?.videoUrl || data?.result || data?.data?.videoUrl;

        if (!videoUrl) {
            await conn.sendMessage(from, { react: { text: '❌', key: mek.key } });
            return await reply(`❌ *Failed to generate video.*\n\nPlease try again with a different prompt.\n\n${config.FOOTER || '© Created By Mr Frank OFC'}`);
        }

        // Prepare caption with config.FOOTER
        const caption = `🎬 *Sora AI Video*\n\n` +
                       `📝 *Prompt:* ${input}\n` +
                       `🤖 *Generated by:* Sora AI Model\n\n` +
                       `${config.FOOTER || '© Created By Mr Frank OFC'}`;

        // Send generated video
        await conn.sendMessage(from, {
            video: { url: videoUrl },
            mimetype: 'video/mp4',
            caption: caption,
            fileName: `sora_ai_video_${Date.now()}.mp4`
        }, { quoted: mek });

        // Success reaction
        await conn.sendMessage(from, { react: { text: '✅', key: mek.key } });

    } catch (err) {
        console.error('Sora CMD Error:', err);
        await conn.sendMessage(from, { react: { text: '❌', key: mek.key } });
        
        const errorMessage = `❌ *Error generating video*\n\n` +
                           `💡 *Possible reasons:*\n` +
                           `• API is temporarily unavailable\n` +
                           `• Prompt was too complex\n` +
                           `• Server timeout\n\n` +
                           `Please try again later with a simpler prompt.\n\n` +
                           `${config.FOOTER || '© Created By Mr Frank OFC'}`;
                           
        return await reply(errorMessage);
    }
});
